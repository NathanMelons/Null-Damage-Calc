#!/usr/bin/env node
/**
 * Lists docs/icons/*.png and writes js/data/icons_manifest.js (src + docs).
 * Run after adding or updating icons: node scripts/generate-icons-manifest.mjs
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const iconsDir = path.join(root, "docs", "icons");

const names = [];
if (fs.existsSync(iconsDir)) {
	for (const f of fs.readdirSync(iconsDir)) {
		if (f.toLowerCase().endsWith(".png")) {
			names.push(f);
		}
	}
	names.sort();
}

const body =
	"/** Auto-generated by scripts/generate-icons-manifest.mjs — do not edit. */\n" +
	"window.POKEMON_ICON_PRELOAD_URLS = " +
	JSON.stringify(names) +
	";\n";

for (const rel of ["src/js/data/icons_manifest.js", "docs/js/data/icons_manifest.js"]) {
	const outPath = path.join(root, rel);
	fs.mkdirSync(path.dirname(outPath), { recursive: true });
	fs.writeFileSync(outPath, body, "utf8");
}

console.log("Wrote icons_manifest.js (" + names.length + " PNG filenames) to src/ and docs/.");
